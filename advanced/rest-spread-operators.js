//rest operator goes in the definition of a function, in order to enable any number of parameters.
const sum = (function() {
    "use strict";
    return function sum(...args) {
      return args.reduce((a, b) => a + b, 0);
    };
  })();
console.log(sum(1, 2, 3)); // 6

